echo "    ${MAGENTA}वापस${RESET} न * फैक्टोरियल(न - 1);"
echo "}"
echo ""
echo "function defined"
echo ""

echo -e "${CYAN}tribhasha>${RESET} ${CYAN}কাৰ্য্য${RESET} ফেক্টৰিয়েল(ন) {"
echo "    ${CYAN}যদি${RESET} (ন <= 1) {"
echo "        ${CYAN}ঘূৰাই_দিয়ক${RESET} 1;"
echo "    }"
echo "    ${CYAN}ঘূৰাই_দিয়ক${RESET} ন * ফেক্টৰিয়েল(ন - 1);"
echo "}"
echo ""
echo "function defined"
echo ""

echo -e "${CYAN}tribhasha>${RESET} factorial(5)"
echo ""
echo "${GREEN}120${RESET}"
echo ""

echo -e "${CYAN}tribhasha>${RESET} फैक्टोरियल(5)"
echo ""
echo "${GREEN}120${RESET}"
echo ""

echo -e "${CYAN}tribhasha>${RESET} ফেক্টৰিয়েল(5)"
echo ""
echo "${GREEN}120${RESET}"
echo ""

echo -e "${CYAN}tribhasha>${RESET} ${BLUE}var${RESET} result = factorial(5) + फैक्टोरियल(3) + ফেক্টৰিয়েল(2);"
echo ""
